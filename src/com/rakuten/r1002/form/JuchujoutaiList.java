package com.rakuten.r1002.form;

import java.io.Serializable;

public class JuchujoutaiList implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String stsdata = null;
	private String joutai = "";
	private String stsid = null;
	private boolean editable = false;

	public String getStsdata() {
		return stsdata;
	}

	public void setStsdata(String stsdata) {
		this.stsdata = stsdata;
	}

	public String getJoutai() {
		return joutai;
	}

	public void setJoutai(String joutai) {
		this.joutai = joutai;
	}

	public boolean isEditable() {
		return editable;
	}

	public void setEditable(boolean editable) {
		this.editable = editable;
	}

	public String getStsid() {
		return stsid;
	}

	public void setStsid(String stsid) {
		this.stsid = stsid;
	}

}
