package com.rakuten.r0701.bean;

import java.io.Serializable;

public class CommodityBean implements Serializable {

	private static final long serialVersionUID = 1L;

	private String commodityId;
	private String kosu = "0";
	private String stockjp = "0";
	private String stocksh = "0";
	private String tochu = "0";
	private String lock = "0";
	private String url;
	private String chineseName = "";
	private String rakuten_url = "";

	/**
	 * @return the commodityId
	 */
	public String getCommodityId() {
		return commodityId;
	}

	/**
	 * @param commodityId
	 *            the commodityId to set
	 */
	public void setCommodityId(String commodityId) {
		this.commodityId = commodityId;
	}

	/**
	 * @return the kosu
	 */
	public String getKosu() {
		return kosu;
	}

	/**
	 * @param kosu
	 *            the kosu to set
	 */
	public void setKosu(String kosu) {
		this.kosu = kosu;
	}

	/**
	 * @return the stockjp
	 */
	public String getStockjp() {
		return stockjp;
	}

	/**
	 * @param stockjp
	 *            the stockjp to set
	 */
	public void setStockjp(String stockjp) {
		this.stockjp = stockjp;
	}

	/**
	 * @return the stocksh
	 */
	public String getStocksh() {
		return stocksh;
	}

	/**
	 * @param stocksh
	 *            the stocksh to set
	 */
	public void setStocksh(String stocksh) {
		this.stocksh = stocksh;
	}

	/**
	 * @return the tochu
	 */
	public String getTochu() {
		return tochu;
	}

	/**
	 * @param tochu
	 *            the tochu to set
	 */
	public void setTochu(String tochu) {
		this.tochu = tochu;
	}

	/**
	 * @return the lock
	 */
	public String getLock() {
		return lock;
	}

	/**
	 * @param lock
	 *            the lock to set
	 */
	public void setLock(String lock) {
		this.lock = lock;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getChineseName() {
		return chineseName;
	}

	public void setChineseName(String chineseName) {
		this.chineseName = chineseName;
	}

	public String getRakuten_url() {
		return rakuten_url;
	}

	public void setRakuten_url(String rakuten_url) {
		this.rakuten_url = rakuten_url;
	}

}
