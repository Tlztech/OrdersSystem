package com.rakuten.r1001.form;

import java.io.Serializable;

public class BunnohasozumiList implements Serializable {

	private static final long serialVersionUID = 1L;

	private String shohinbango = null;
	private String kosu = null;
	private String hasonichiji = null;
	private String haisokaisha = null;
	private String haisohoho = null;
	private String toiawasebango = null;

	/**
	 * @return the shohinbango
	 */
	public String getShohinbango() {
		return shohinbango;
	}

	/**
	 * @param shohinbango
	 *            the shohinbango to set
	 */
	public void setShohinbango(String shohinbango) {
		this.shohinbango = shohinbango;
	}

	/**
	 * @return the kosu
	 */
	public String getKosu() {
		return kosu;
	}

	/**
	 * @param kosu
	 *            the kosu to set
	 */
	public void setKosu(String kosu) {
		this.kosu = kosu;
	}

	/**
	 * @return the hasonichiji
	 */
	public String getHasonichiji() {
		return hasonichiji;
	}

	/**
	 * @param hasonichiji
	 *            the hasonichiji to set
	 */
	public void setHasonichiji(String hasonichiji) {
		this.hasonichiji = hasonichiji;
	}

	/**
	 * @return the haisokaisha
	 */
	public String getHaisokaisha() {
		return haisokaisha;
	}

	/**
	 * @param haisokaisha
	 *            the haisokaisha to set
	 */
	public void setHaisokaisha(String haisokaisha) {
		this.haisokaisha = haisokaisha;
	}

	/**
	 * @return the haisohoho
	 */
	public String getHaisohoho() {
		return haisohoho;
	}

	/**
	 * @param haisohoho
	 *            the haisohoho to set
	 */
	public void setHaisohoho(String haisohoho) {
		this.haisohoho = haisohoho;
	}

	/**
	 * @return the toiawasebango
	 */
	public String getToiawasebango() {
		return toiawasebango;
	}

	/**
	 * @param toiawasebango
	 *            the toiawasebango to set
	 */
	public void setToiawasebango(String toiawasebango) {
		this.toiawasebango = toiawasebango;
	}

}
